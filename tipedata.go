package main

import "fmt"

func main() {
	//Boolean
	fmt.Println("Benar = ", true)
	fmt.Println("Salah = ", false)

	//String
	fmt.Println("Sehan")
	fmt.Println("Muhamad Sehan")
	fmt.Println("Muhamad Sehan Alfarisi")

	//String Function
	fmt.Println(len("Sehan"))
	fmt.Println("Muhamad Sehan"[0])
	fmt.Println("Muhamad Sehan Alfarisi"[1])
}
